/**
 * _memcpy -  function that copies memory area
 *
 * @src: first input
 * @dest: second input
 * @n: third input
 *
 * Return: Always 0
*/

char *_memcpy(char *dest, char *src, unsigned int n)
{
unsigned int itr;
for (itr = 0; itr < 0; itr++)
{
dest[itr] = src[itr];
}
return (dest);
}
